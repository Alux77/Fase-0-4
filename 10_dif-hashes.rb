# diferencia entre hashes
key = "Lets Code"
hash = {key: "The Future"}
hash2 = {key => "The Future"}

p key
p hash
p hash2

=begin
 la diferencia radica en las "key"
 1er hash (:key) = palabra clave que tiene como valor "The Future"
 2º hash key = variable (se manda llamar) definida en el 1er renglón con valor "Lets Code"
=end